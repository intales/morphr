import 'dart:convert';
import 'dart:io';

import 'package:args/command_runner.dart';
import 'package:interact/interact.dart';
import 'package:http/http.dart' as http;
import '../helpers/config_helper.dart';

class InitCommand extends Command {
  @override
  final name = 'init';
  @override
  final description = 'Initialize a Morphr project and link it to a Figma file';

  InitCommand() {
    argParser
      ..addOption(
        'figma-url',
        abbr: 'f',
        help: 'URL of your Figma file',
      )
      ..addOption(
        'server',
        abbr: 's',
        help: 'Morphr Cloud server (only for testing)',
        defaultsTo: 'https://cloud.morphr.dev',
      );
  }

  @override
  Future<void> run() async {
    var figmaUrl = argResults?['figma-url'] as String?;
    final server = argResults?['server'] as String;

    print('\nðŸŽ¨ Welcome to Morphr! Let\'s set up your project.\n');

    final token = ConfigHelper.getToken();
    if (token == null) {
      print(
          'âŒ No authentication token found. Please login first with: morphr login');
      exit(1);
    }

    figmaUrl ??= Input(
      prompt: 'ðŸ”— Paste your Figma file URL',
      validator: (value) {
        if (value.isEmpty) {
          throw ValidationError('âŒ URL cannot be empty');
        }
        if (!value.contains('figma.com')) {
          throw ValidationError('âŒ Not a valid Figma URL');
        }
        return true;
      },
    ).interact();

    final figmaFileId = _extractFigmaFileId(figmaUrl);
    if (figmaFileId == null) {
      print(
          '\nâŒ Could not extract file ID from URL. Please check the URL and try again.');
      exit(1);
    }

    print('\nðŸ” Verifying access to Figma file...');
    final isAccessible = await _verifyFileAccess(server, token, figmaFileId);
    if (!isAccessible) {
      print('\nâŒ Could not access this Figma file.');
      print(
          'Make sure you have linked your Figma account and have access to the file.');
      exit(1);
    }

    final fileDetails = await _getFileDetails(server, token, figmaFileId);
    final fileName = fileDetails?['name'] ?? 'Flutter Project';

    final projectName = Input(
      prompt: 'ðŸ“ Enter a name for your Morphr project',
      defaultValue: fileName,
    ).interact();

    print('\nðŸš€ Creating project on Morphr Cloud...');
    final projectId =
        await _createProject(server, token, projectName, figmaFileId);
    if (projectId == null) {
      print('\nâŒ Failed to create project on Morphr Cloud.');
      exit(1);
    }

    final directory = Directory('lib');
    if (!await directory.exists()) {
      await directory.create(recursive: true);
    }

    final configFile = File('lib/morphr_options.dart');
    await configFile.writeAsString('''
// File generated by Morphr CLI - do not modify manually

/// Morphr configuration options
